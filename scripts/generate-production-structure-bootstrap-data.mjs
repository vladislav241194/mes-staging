function extractExport(source, exportName) {
  const marker = `export const ${exportName} = `;
  const start = source.indexOf(marker);
  if (start < 0) throw new Error(`Export ${exportName} was not found in ${sourcePath}`);
  const expressionStart = start + marker.length;
  const expressionEnd = source.indexOf(";\n", expressionStart);
  if (expressionEnd < 0) throw new Error(`Export ${exportName} has no terminating semicolon`);
  return JSON.parse(source.slice(expressionStart, expressionEnd));
}

export async function syncProductionStructureBootstrapData() {
  const source = await readFile(sourcePath, "utf-8");
  const columns = extractExport(source, "PRODUCTION_STRUCTURE_MATRIX_COLUMNS");
  const rows = extractExport(source, "PRODUCTION_STRUCTURE_MATRIX_ROWS");
  const allowed = new Set(BOOTSTRAP_CELL_KEYS);
  const bootstrapRows = rows.map(({ id, sourceRow, level, cells = {} }) => [
    id,
    sourceRow,
    level,
    BOOTSTRAP_CELL_KEYS.map((key) => allowed.has(key) ? String(cells[key] ?? "") : ""),
  ]);
  const output = `// Generated by scripts/generate-production-structure-bootstrap-data.mjs. Do not edit manually.\nexport const PRODUCTION_STRUCTURE_BOOTSTRAP_COLUMNS = ${JSON.stringify(columns)};\nexport const PRODUCTION_STRUCTURE_BOOTSTRAP_FIELD_COUNT = ${columns.length};\nconst BOOTSTRAP_CELL_KEYS = ${JSON.stringify(BOOTSTRAP_CELL_KEYS)};\nconst COMPACT_BOOTSTRAP_ROWS = ${JSON.stringify(bootstrapRows)};\nexport const PRODUCTION_STRUCTURE_BOOTSTRAP_ROWS = COMPACT_BOOTSTRAP_ROWS.map(([id, sourceRow, level, values]) => ({ id, sourceRow, level, cells: Object.fromEntries(BOOTSTRAP_CELL_KEYS.map((key, index) => [key, values[index]])) }));\n`;
  await writeFile(outputPath, output, "utf-8");
  // `data.js` needs only the planning work-centre projection on the first
  // paint. Keep it separate from the full editor/migration matrix, whose
  // repeated explanatory strings are intentionally loaded on demand.
  const { getProductionStructureWorkCenters } = await import(new URL("../src/production_structure_service.js", import.meta.url));
  const defaultWorkCenters = getProductionStructureWorkCenters();
  await writeFile(
    defaultWorkCentersOutputPath,
    `// Generated by scripts/generate-production-structure-bootstrap-data.mjs. Do not edit manually.\nexport const DEFAULT_PRODUCTION_WORK_CENTERS = ${JSON.stringify(defaultWorkCenters)};\n`,
    "utf-8",
  );
  return { rows: bootstrapRows.length, fields: columns.length, outputPath, defaultWorkCentersOutputPath };
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  const result = await syncProductionStructureBootstrapData();
  console.log(`Generated bootstrap production structure: ${result.rows} rows, ${result.fields} source fields; ${result.defaultWorkCentersOutputPath}.`);
}
