import { readdir, readFile, writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import { dirname, join, relative } from "node:path";
import {
  MES_ICON_ALIASES,
  MES_ICON_ENTRIES,
  MES_ICON_RUNTIME_ALIASES,
  MES_ICON_SVG_BY_SLUG,
} from "../src/icons/registry.js";
import { MES_MODULE_BLUEPRINT_REGISTRY } from "../src/module_registry.js";

const projectRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const sourceRoot = join(projectRoot, "src");
const outputPath = join(sourceRoot, "icons", "runtime_registry.js");
const customSvgOutputPath = join(sourceRoot, "icons", "runtime_custom_svg_registry.js");
const excludedFiles = new Set([
  "icons/registry.js",
  "icons/runtime_registry.js",
]);

async function collectJsFiles(directory) {
  const entries = await readdir(directory, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const filePath = join(directory, entry.name);
    if (entry.isDirectory()) files.push(...await collectJsFiles(filePath));
    else if (entry.isFile() && entry.name.endsWith(".js")) files.push(filePath);
  }
  return files;
}

function collectLiteralIconNames(source) {
  const names = new Set();
  // Read only literals inside the call argument. Scanning every quoted value
  // on a source line breaks for icon calls nested in template literals (for
  // example the legacy directory delete button) and can silently drop the
  // real icon name after quote pairing crosses the nested template boundary.
  for (const call of source.matchAll(/\b(?:icon|getMesCustomIconName)\s*\(([^)]*)\)/g)) {
    // Calls frequently select between two literal names in a ternary. Keep
    // both names rather than silently degrading the less common branch.
    for (const match of call[1].matchAll(/["']([^"']+)["']/g)) names.add(match[1]);
  }
  // Custom MES icons have semantic fallbacks used when their optional SVG
  // chunk is unavailable. Those literals sit outside getMesCustomIconName().
  for (const fallback of source.matchAll(/\bgetMesCustomIconName\s*\([^)]*\)\s*\|\|\s*["']([^"']+)["']/g)) {
    names.add(fallback[1]);
  }
  return names;
}

function normalizeIconName(value) {
  const key = String(value || "").trim();
  if (!key) return "";
  return MES_ICON_ALIASES[key] || MES_ICON_ALIASES[key.toLowerCase()] || "";
}

export async function syncMesIconRuntimeRegistry() {
  // Blueprint navigation is rendered dynamically, so its icon names cannot
  // be discovered by the literal `icon(...)` source scan below. Seed them
  // explicitly to keep the runtime registry complete without loading the
  // full icon catalog on every startup.
  const requestedNames = new Set([
    ...Object.values(MES_ICON_RUNTIME_ALIASES),
    ...MES_MODULE_BLUEPRINT_REGISTRY.map(({ icon }) => icon),
    // Generated/minified current modules receive the icon renderer through a
    // short local binding, so their edit/filter calls are not discoverable as
    // literal `icon(...)` calls in source.
    "edit",
    "filter",
  ]);
  const files = await collectJsFiles(sourceRoot);
  for (const filePath of files) {
    const projectRelativePath = relative(sourceRoot, filePath).replaceAll("\\", "/");
    if (excludedFiles.has(projectRelativePath)) continue;
    for (const name of collectLiteralIconNames(await readFile(filePath, "utf-8"))) requestedNames.add(name);
  }
  requestedNames.add("info");

  const semanticSlugs = new Set([...requestedNames].map(normalizeIconName).filter(Boolean));
  const svgBySlug = Object.fromEntries([...semanticSlugs]
    .sort()
    .filter((slug) => MES_ICON_ENTRIES.find((entry) => entry.semanticSlug === slug)?.source !== "custom-svg")
    .map((slug) => [slug, MES_ICON_SVG_BY_SLUG[slug]])
    .filter(([, svg]) => Boolean(svg)));
  const customSvgBySlug = Object.fromEntries([...semanticSlugs]
    .sort()
    .filter((slug) => MES_ICON_ENTRIES.find((entry) => entry.semanticSlug === slug)?.source === "custom-svg")
    .map((slug) => [slug, MES_ICON_SVG_BY_SLUG[slug]])
    .filter(([, svg]) => Boolean(svg)));
  const entries = MES_ICON_ENTRIES
    .filter((entry) => semanticSlugs.has(entry.semanticSlug))
    .map(({ semanticSlug, source, referenceAsset }) => ({ semanticSlug, source, referenceAsset }));
  const aliases = Object.fromEntries(Object.entries(MES_ICON_ALIASES)
    .filter(([, slug]) => semanticSlugs.has(slug)));

  const output = `// Generated by scripts/generate-mes-icon-runtime-registry.mjs. Do not edit manually.\nexport const MES_RUNTIME_ICON_ENTRIES = ${JSON.stringify(entries)};\nexport const MES_RUNTIME_ICON_SVG_BY_SLUG = ${JSON.stringify(svgBySlug)};\nexport const MES_RUNTIME_ICON_ALIASES = ${JSON.stringify(aliases)};\nexport const MES_RUNTIME_ICON_WORKCENTER_ALIASES = ${JSON.stringify(MES_ICON_RUNTIME_ALIASES)};\nconst ENTRY_BY_SLUG = new Map(MES_RUNTIME_ICON_ENTRIES.map((entry) => [entry.semanticSlug, entry]));\nconst NORMALIZED_ALIAS_BY_KEY = new Map(Object.entries(MES_RUNTIME_ICON_ALIASES).map(([alias, target]) => [String(alias).toLowerCase(), target]));\nlet customSvgBySlug = null;\nlet customSvgLoadPromise = null;\nexport function getMesRuntimeIconName(value) { const key = String(value || \"\").trim(); return key ? NORMALIZED_ALIAS_BY_KEY.get(key.toLowerCase()) || (ENTRY_BY_SLUG.has(key) ? key : \"\") : \"\"; }\nexport function getMesRuntimeIconEntry(value) { const slug = getMesRuntimeIconName(value); return slug ? ENTRY_BY_SLUG.get(slug) || null : null; }\nexport function getMesRuntimeIconSvg(value) { const slug = getMesRuntimeIconName(value); return slug ? MES_RUNTIME_ICON_SVG_BY_SLUG[slug] || customSvgBySlug?.[slug] || \"\" : \"\"; }\nexport function loadMesRuntimeCustomIconSvgs() { if (customSvgBySlug) return Promise.resolve(customSvgBySlug); if (!customSvgLoadPromise) customSvgLoadPromise = import(\"./runtime_custom_svg_registry.js\").then((module) => { customSvgBySlug = module.MES_RUNTIME_CUSTOM_ICON_SVG_BY_SLUG || {}; return customSvgBySlug; }); return customSvgLoadPromise; }\nexport function getMesRuntimeIconNameForRuntimeId(runtimeId) { return MES_RUNTIME_ICON_WORKCENTER_ALIASES[String(runtimeId || \"\")] || \"\"; }\n`;
  const customSvgOutput = `// Generated by scripts/generate-mes-icon-runtime-registry.mjs. Do not edit manually.\nexport const MES_RUNTIME_CUSTOM_ICON_SVG_BY_SLUG = ${JSON.stringify(customSvgBySlug)};\n`;
  await writeFile(outputPath, output, "utf-8");
  await writeFile(customSvgOutputPath, customSvgOutput, "utf-8");
  return { icons: entries.length, aliases: Object.keys(aliases).length, outputPath };
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  const result = await syncMesIconRuntimeRegistry();
  console.log(`Generated runtime icon registry: ${result.icons} icons, ${result.aliases} aliases.`);
}
