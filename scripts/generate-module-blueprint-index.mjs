import { mkdir, readdir, readFile, stat, writeFile } from "node:fs/promises";
import { dirname, join, relative, resolve, sep } from "node:path";
import { fileURLToPath } from "node:url";

const scriptPath = fileURLToPath(import.meta.url);
const projectRoot = join(dirname(scriptPath), "..");

function toPosix(value) {
  return value.split(sep).join("/");
}

function toIdentifier(value = "") {
  const parts = String(value).split(/[^a-zA-Z0-9]+/).filter(Boolean);
  const identifier = parts.map((part, index) => (
    index === 0
      ? `${part.charAt(0).toLowerCase()}${part.slice(1)}`
      : `${part.charAt(0).toUpperCase()}${part.slice(1)}`
  )).join("");
  return /^[A-Za-z_$]/.test(identifier) ? identifier : `module${identifier}`;
}

async function pathExists(path) {
  try {
    await stat(path);
    return true;
  } catch {
    return false;
  }
}

async function discoverBlueprintModules(modulesRoot) {
  const entries = await readdir(modulesRoot, { withFileTypes: true });
  const modules = [];
  for (const entry of entries) {
    if (!entry.isDirectory()) continue;
    const blueprintPath = join(modulesRoot, entry.name, "blueprint.js");
    if (!(await pathExists(blueprintPath))) continue;
    const runtimePath = join(modulesRoot, entry.name, "runtime.js");
    if (!(await pathExists(runtimePath))) {
      throw new Error(`Blueprint module ${entry.name} is missing src/modules/${entry.name}/runtime.js`);
    }
    modules.push({
      directory: entry.name,
      identifier: toIdentifier(entry.name),
      blueprintPath,
      runtimePath,
    });
  }
  const sortedModules = modules.sort((left, right) => left.directory.localeCompare(right.directory));
  const duplicateIdentifiers = sortedModules
    .map(({ identifier }) => identifier)
    .filter((identifier, index, identifiers) => identifiers.indexOf(identifier) !== index);
  if (duplicateIdentifiers.length) {
    throw new Error(`Blueprint module directory identifiers collide: ${[...new Set(duplicateIdentifiers)].join(", ")}`);
  }
  return sortedModules;
}

function renderBlueprintIndex(modules) {
  const imports = modules.map(({ directory, identifier }) => (
    `import { MODULE_BLUEPRINT as ${identifier}Blueprint } from "../modules/${directory}/blueprint.js";`
  )).join("\n");
  const entries = modules.map(({ identifier }) => `  ${identifier}Blueprint,`).join("\n");
  return `/* Generated by scripts/generate-module-blueprint-index.mjs. Do not edit manually. */\n${imports}\n\nexport const GENERATED_MODULE_BLUEPRINTS = Object.freeze([\n${entries}\n]);\n`;
}

function renderRuntimeIndex(modules) {
  const imports = modules.map(({ directory, identifier }) => {
    const pascalIdentifier = `${identifier.charAt(0).toUpperCase()}${identifier.slice(1)}`;
    return `import { MODULE_BLUEPRINT as ${identifier}Blueprint } from "../modules/${directory}/blueprint.js";\nimport { createModuleRuntimeAdapter as create${pascalIdentifier}RuntimeAdapter } from "../modules/${directory}/runtime.js";`;
  }).join("\n");
  const entries = modules.map(({ identifier }) => (
    `    [${identifier}Blueprint.id]: create${identifier.charAt(0).toUpperCase()}${identifier.slice(1)}RuntimeAdapter(context),`
  )).join("\n");
  return `/* Generated by scripts/generate-module-blueprint-index.mjs. Do not edit manually. */\n${imports}\n\nexport function createGeneratedModuleRuntimeAdapters(context = {}) {\n  return Object.freeze({\n${entries}\n  });\n}\n`;
}

export async function syncGeneratedModuleBlueprintIndexes({ check = false, targetRoot = projectRoot } = {}) {
  const resolvedRoot = resolve(targetRoot);
  const modulesRoot = join(resolvedRoot, "src", "modules");
  const generatedRoot = join(resolvedRoot, "src", "generated");
  const blueprintIndexPath = join(generatedRoot, "module_blueprint_index.js");
  const runtimeIndexPath = join(generatedRoot, "module_runtime_index.js");
  const modules = await discoverBlueprintModules(modulesRoot);
  if (!check) await mkdir(generatedRoot, { recursive: true });
  const expected = new Map([
    [blueprintIndexPath, renderBlueprintIndex(modules)],
    [runtimeIndexPath, renderRuntimeIndex(modules)],
  ]);
  const drift = [];
  for (const [path, source] of expected) {
    const current = await readFile(path, "utf8").catch(() => "");
    if (current === source) continue;
    if (check) {
      drift.push(toPosix(relative(resolvedRoot, path)));
      continue;
    }
    await writeFile(path, source);
  }
  if (drift.length) {
    throw new Error(`Generated module blueprint index drift: ${drift.join(", ")}. Run npm run module:blueprints:sync.`);
  }
  return {
    modules: modules.map(({ directory }) => directory),
    files: [...expected.keys()].map((path) => toPosix(relative(resolvedRoot, path))),
    changed: check ? [] : undefined,
  };
}

if (process.argv[1] && resolve(process.argv[1]) === scriptPath) {
  const result = await syncGeneratedModuleBlueprintIndexes({ check: process.argv.includes("--check") });
  console.log(`Module blueprint index: ${result.modules.length} generated module(s): ${result.modules.join(", ") || "none"}`);
}
